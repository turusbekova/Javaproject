package com.java.practice;

public class JavaOperators {
    public static void main(String[] args) {
        // Java Operators:

        // Arithmetic Operators :
        // + --> addition
        int num = 10;
        int num2 = 3;
        System.out.println(num + num2);
        // - --> subtraction
        System.out.println(num - num2);
        // * --> multiplication

        // / --> division
        System.out.println(num / num2);
        // % --> modulo
        System.out.println(num % num2); // 10 % 3 = 3.1






    }
}
